const mongoose = require('mongoose');

const scriptSchema = new mongoose.Schema({
    id: {
        type: String,
        required: true,
        unique: true
    },
    title: {
        type: String,
        required: true
    },
    content: {
        type: String,
        required: true
    },
    category: {
        type: String,
        required: true,
        enum: ['game', 'gui', 'admin', 'utility', 'other']
    },
    favorite: {
        type: Boolean,
        default: false
    },
    thumbnail: {
        type: String,
        default: ''
    },
    createdAt: {
        type: Date,
        default: Date.now
    },
    updatedAt: {
        type: Date,
        default: Date.now
    }
});

module.exports = mongoose.model('Script', scriptSchema); 